package com.onetopic.fp.methodrefer;

interface Callable { // [1]
    void call(String s);
}

class Describe {
    void show(String msg) { // [2]
        System.out.println(msg);
    }
}

public class MethodReferences {
    static void hello(String name) { // [3]
        System.out.println("Hello, " + name);
    }
    static class Description {
        String about;
        Description(String desc) { about = desc; }
        void help(String msg) { // [4]
            System.out.println(about + " " + msg);
        }
    }
    static class Helper {
        static void assist(String msg) { // [5]
            System.out.println(msg);
        }
    }
    public static void main(String[] args) {
        Describe d = new Describe();
        Callable c = d::show; // [6] show() 的签名（参数类型和返回类型）符合 Callable 的 call() 的签名。
        c.call("call()"); // [7]

        c = MethodReferences::hello; // [8]hello() 也符合 call() 的签名
        c.call("Bob");

        c = new Description("valuable")::help; // [9] help() 也符合，它是静态内部类中的非静态方法。
        c.call("information");

        c = Helper::assist; // [10] assist() 是静态内部类中的静态方法
        c.call("Help!");
    }
}
